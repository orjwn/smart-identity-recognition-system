export function normalizeLocale(code: string | undefined | null): Locale {
  const c = (code || "en").toLowerCase().split("-")[0];
  if (SUPPORTED_LOCALES.includes(c as Locale)) return c as Locale;
  return "en";
}

export function t(locale: Locale, key: string, vars?: Record<string, string | number>): string {
  let s = STRINGS[locale]?.[key] ?? STRINGS.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`{{${k}}}`, "g"), String(v));
    }
  }
  return s;
}

export function translateFlightStatus(locale: Locale, status: string | undefined): string {
  if (!status) return "";
  const k = status.trim().toLowerCase();
  const map: Record<string, string> = {
    "on time": "flightStatus_onTime",
    boarding: "flightStatus_boarding",
    delayed: "flightStatus_delayed",
    cancelled: "flightStatus_cancelled",
    departed: "flightStatus_departed",
  };
  const key = map[k];
  return key ? t(locale, key) : status;
}

export function formatKioskError(locale: Locale, error: string | null | undefined): string {
  if (!error) return "";
  const key = `error_${error}`;
  const translated = t(locale, key);
  return translated === key ? error : translated;
}

export function formatTime(iso: string | undefined, locale: Locale): string {
  if (!iso) return "";
  const d = new Date(iso);
  if (Number.isNaN(d.getTime())) return iso;
  return new Intl.DateTimeFormat(locale === "ar" ? "ar" : locale, {
    hour: "2-digit",
    minute: "2-digit",
  }).format(d);
}
